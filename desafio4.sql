SELECT JOB_TITLE AS Cargo,
ROUND(AVG(MAX_SALARY - MIN_SALARY), 2) AS 'Média salarial',
CASE
WHEN AVG(MAX_SALARY - MIN_SALARY) BETWEEN 2000 AND 5800 THEN 'Júnior'
WHEN AVG(MAX_SALARY - MIN_SALARY) BETWEEN 5801 AND 7500 THEN 'Pleno'
WHEN AVG(MAX_SALARY - MIN_SALARY) BETWEEN 7501 AND 10500 THEN 'Sênior'
WHEN AVG(MAX_SALARY - MIN_SALARY) > 10500 THEN 'CEO'
END AS 'Senioridade'
FROM hr.jobs
GROUP BY Cargo
ORDER BY ROUND(AVG(MAX_SALARY - MIN_SALARY), 2), Cargo;
